class RestaurantDetail {
  String name;
  String imageUrl;
  String phone;
  double rating;
  String city;
  String address;
  String url;

  RestaurantDetail(String name, String imageUrl, String phone, double rating, String city, String address, String url) {
    this.name = name;
    this.imageUrl = imageUrl;
    this.phone = phone;
    this.rating = rating;
    this.city = city;
    this.address = address;
    this.url = url;
  }
}